using LogLib.Abstractions;

namespace LogLib.Context;

/// <summary>
/// Реализация провайдера контекста вызова логирования.
/// 
/// Отвечает за возврат пути к файлу и номера строки, откуда был
/// вызван метод логирования. В базовой реализации просто возвращает
/// переданные значения без дополнительной обработки.
/// 
/// При необходимости сюда можно добавить:
/// - сокращение полного пути до имени файла;
/// - фильтрацию служебных путей;
/// - нормализацию формата пути для разных ОС.
/// </summary>
public class CallerInfoContextProvider : ILogContextProvider
{
  /// <summary>
  /// Возвращает путь к файлу и номер строки вызова.
  /// </summary>
  /// <param name="file">Путь к файлу, автоматически подставляется компилятором при вызове.</param>
  /// <param name="line">Номер строки, автоматически подставляется компилятором при вызове.</param>
  public (string file, int line) GetCallerInfo(string file = "", int line = 0)
  {
    return (file, line);
  }
}