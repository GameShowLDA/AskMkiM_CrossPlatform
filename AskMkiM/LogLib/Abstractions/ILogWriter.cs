namespace LogLib.Abstractions;

/// <summary>
/// Низкоуровневый интерфейс записи логов.
/// Реализация (например, NLogWriter) выполняет физическую запись
/// сообщения в файл, систему мониторинга, консоль или любую
/// другую целевую платформу.
///
/// LogService формирует структуру LogEntry и передаёт её в writer.
/// Writer не выполняет логики обработки уровней, формирования текста
/// или выбора категории — он только осуществляет запись.
/// </summary>
public interface ILogWriter
{
  /// <summary>
  /// Записывает подготовленную структуру лог-сообщения.
  /// </summary>
  /// <param name="entry">Структура лог-сообщения.</param>
  void Write(LogEntry entry);
}