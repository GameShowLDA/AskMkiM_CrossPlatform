namespace Ask.Shared.Enums.Common;

/// <summary>
/// Определяет возможные действия пользователя при возникновении ошибки
/// или иной ситуации, требующей выбора дальнейшего поведения системы.
/// Используется исполнителем программ контроля, диалоговыми сервисами,
/// а также механизмами обработки ошибок.
/// </summary>
public enum UserAction
{
  /// <summary>
  /// Повторить выполнение операции.
  /// </summary>
  Retry,

  /// <summary>
  /// Продолжить выполнение, игнорируя текущую ошибку.
  /// </summary>
  Continue,

  /// <summary>
  /// Прервать выполнение текущей процедуры или программы.
  /// </summary>
  Abort,

  /// <summary>
  /// Действие пользователя не определено или не требуется.
  /// </summary>
  None,
}
