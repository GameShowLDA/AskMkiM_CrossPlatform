namespace Ask.Shared.Enums.Common;

/// <summary>
/// Определяет категорию сообщения, формируемого исполнителем программ контроля,
/// протоколом выполнения или подсистемами оборудования. Используется для выбора
/// логики отображения сообщения в пользовательском интерфейсе, в том числе
/// для настройки цветовой схемы, приоритетов, форматирования и группировки.
///
/// Значения перечисления отражают различную семантику сообщений:
/// - <see cref="Command"/> — единичная команда из управляющей программы.
/// - <see cref="CommandBlock"/> — логическая группа последовательных команд.
/// - <see cref="Info"/> — общее информационное сообщение без ошибок.
/// - <see cref="Success"/> — успешный результат выполнения операции.
/// - <see cref="Error"/> — сообщение об ошибке, возникшей в ходе выполнения.
/// 
/// Перечисление не содержит визуальных параметров и не зависит от платформы.
/// Конкретные правила отображения (цвет, иконки, оформление) определяются
/// уровнем пользовательского интерфейса.
/// </summary>
public enum MessageType
{
  /// <summary>
  /// Информационное сообщение, представляющее отдельную команду
  /// управляющей программы. Используется для пошагового отображения
  /// текущего состояния работы алгоритма.
  /// </summary>
  Command,

  /// <summary>
  /// Сообщение, обозначающее блок команд или логически объединённый
  /// фрагмент управляющей программы. Может использоваться для
  /// визуального выделения групп операций.
  /// </summary>
  CommandBlock,

  /// <summary>
  /// Общая информационная запись, не содержащая ошибок. Применяется
  /// для отображения статуса, хода выполнения или вспомогательных сведений.
  /// </summary>
  Info,

  /// <summary>
  /// Сообщение об успешном завершении операции. Используется для
  /// подтверждения корректного выполнения метода или шага программы.
  /// </summary>
  Success,

  /// <summary>
  /// Сообщение, указывающее на ошибку выполнения. Применяется как для
  /// ошибок алгоритма, так и для ошибок оборудования или параметров.
  /// Может инициировать выбор действия пользователя.
  /// </summary>
  Error
}